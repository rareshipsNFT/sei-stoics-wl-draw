use rand::{Rng, SeedableRng};

fn main() {
    // combine the entropy from the rarenauts
    let entropy: Vec<u64> = vec![555, 2000, (1<<32)-1, 69];
    let sum = entropy.iter().sum::<u64>();

    // init a pseudo random generator
    let mut rng = rand::rngs::StdRng::seed_from_u64(sum);
    
    // collect 50 random ship holders
    let mut winners: Vec<&str> = Vec::new();
    while winners.len() < 50 {
        let winner = SHIP_HOLDERS[rng.gen_range(0..SHIP_HOLDERS.len())];
        if !winners.contains(&winner) {
            winners.push(winner);
        }
    }
    
    // print the winners
    println!("The winners are:");
    for winner in winners {
        println!("{}", winner);
    }
}

